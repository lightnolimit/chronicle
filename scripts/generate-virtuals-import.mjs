#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import { pathToFileURL } from "node:url";
import { ensureParentDir, parseDotenvObject } from "./lib/dotenv-utils.mjs";

const OUTPUT_DIR = "dev-docs/generated/virtuals";
const PROD_ENV_FILE = ".env.phala";
const GENERATED_NOTICE = "Generated by `node scripts/generate-virtuals-import.mjs`. Do not edit manually.";

function applyEnvMap(envMap) {
  for (const [key, value] of Object.entries(envMap)) {
    if (!(key in process.env)) {
      process.env[key] = value;
    }
  }
}

async function loadCatalog() {
  const envText = await fs.readFile(PROD_ENV_FILE, "utf8");
  applyEnvMap(parseDotenvObject(envText));

  const modulePath = pathToFileURL(path.resolve("agent/dist/src/acp/catalog.js")).href;
  const catalogModule = await import(modulePath);
  return catalogModule.getAcpCatalog({ includeDisabled: false });
}

function buildJobs(catalog) {
  return catalog.map((offering) => ({
    name: offering.id,
    description: offering.description,
    requiredFunds: false,
    price: {
      type: "fixed",
      value: offering.priceUsd,
    },
    slaMinutes: offering.slaMinutes,
    requirement: offering.requirementSchema,
    deliverable: offering.deliverableShape,
  }));
}

function buildResources() {
  return [
    {
      name: "chronicle_catalog",
      description: "Fetch Chronicle's current ACP offering catalog and enabled status.",
      url: "https://api.chronicle.sh/api/acp/catalog",
      params: {
        type: "object",
        properties: {},
        required: [],
        additionalProperties: false,
      },
    },
    {
      name: "chronicle_price_estimator",
      description: "Estimate Chronicle storage pricing by payload size in bytes.",
      url: "https://api.chronicle.sh/api/price?size={{size}}",
      params: {
        type: "object",
        properties: {
          size: {
            type: "number",
            description: "Payload size in bytes.",
          },
        },
        required: ["size"],
        additionalProperties: false,
      },
    },
  ];
}

function wrapGeneratedPayload(payload) {
  return {
    _comment: GENERATED_NOTICE,
    ...payload,
  };
}

async function writeJson(filePath, value) {
  await ensureParentDir(filePath);
  await fs.writeFile(filePath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
  console.log(`Generated ${filePath}`);
}

async function main() {
  const catalog = await loadCatalog();
  const jobs = buildJobs(catalog);
  const resources = buildResources();

  await writeJson(path.join(OUTPUT_DIR, "chronicle.jobs.json"), wrapGeneratedPayload({ jobs }));
  await writeJson(
    path.join(OUTPUT_DIR, "chronicle.resources.json"),
    wrapGeneratedPayload({ resources }),
  );
  await writeJson(
    path.join(OUTPUT_DIR, "chronicle.jobs-and-resources.json"),
    wrapGeneratedPayload({ jobs, resources }),
  );
}

main().catch((error) => {
  console.error(error.message || error);
  process.exitCode = 1;
});
